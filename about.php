<h2>Concept:</h2>
	<ul>
		<li>1.	Convert CS/HLSW log files to XML documents called .GCS documents</li>
		<li>2.	Create GCS standard documentation</li>
		<li>3.	Create GCS reader</li>
		<li>4.	Create GCS -> HTML parse</li>
		<li>5.	HTML parse will display legible statistics</li>
	</ul>
<h2>Functions:</h2>
	<ul>
		<li>1.	Generate legible Game Log, (Round 1: xx killed yy… Bomb Defused CT+1)</li>
		<li>2.	Overall score display</li>
		<li>3.	Half score displays (including overtime halves)</li>
		<li>4.	Round score display </li>
		<li>5.	Display the way in which round’s were won/completed (Defuse, Kill, Time, etc..)</li>
		<li>6.	Provide interesting graphical interface</li>
		<li>7.	Individual player statistics displayed by alias</li>
		<li>8.	Individual player statistics including but not limited to:
			<ul>
				<li>a.	Frags</li>
				<li>b.	Deaths</li>
				<li>c.	Assists</li>
				<li>d.	Errors</li>
				<li>e.	Round(s) with first frag, a.k.a. “Entry Frags”</li>
				<li>f.	Round(s) with first death</li>
				<li>g.	Round(s) with 2 frags</li>
				<li>h.	Round(s) with 3 frags</li>
				<li>i.	Round(s) with 4 frags</li>
				<li>j.	Round(s) with 5 frags, a.k.a. “Aces”</li>
				<li>k.	Average damage per round (total damage / # rounds)</li>
				<li>l.	Rounds with a frag </li>
				<li>m.	% Rounds with a frag (# rounds with 1+ frag(s) / # rounds)</li>
				<li>n.	Frags per round (# frags / # rounds)</li>
				<li>o.	% Team Frags (# frags / # team’s frags)</li>
				<li>p.	+/- (# of frags - # of deaths)</li>
				<li>q.	% of Frags in round(s) team won (# frags in rounds won / # total frags)</li>
				<li>r.	% of Frags in round(s) team lost (# frags in rounds lost / # total frags)</li>
				<li>s.	Player Point Scorecard 
					<ul>
						<li>i.	Frags +2</li>
						<li>ii.	Assists +1</li>
						<li>iii.	Errors -1</li>
						<li>iv.	First Frag +0.5</li>
						<li>v.	First Death -0.5</li>
						<li>vi.	Bomb Plants +0.5</li>
						<li>vii.	Successful Bombs +0.5</li>
						<li>viii.	Bomb Defuses +1</li>
					</ul>
				</li>
			</ul>
		</li>
		<li>9.	Team totals for each statistical category</li>
	</ul>
<h2>Future Enhancements:</h2>
	<ul>
		<li>1.	Statistics stored within central database</li>
		<li>2.	Central database searchable</li>
		<li>3.	Differentiate Eco to Gun rounds based on each player damage dealt gun, if no damage dealt, assume eco.  If 3 or more players are found to be eco, assume that the round is an eco.</li>
		<li>4.	In accordance to advancement #3, display statistics based on how many frags were achieved during an eco round, and how many frags were achieved in anti-eco rounds.</li>
		<li>5.	Create a “clutch-meter”.  Takes into account players in 1 vs X setting and tests the amount that they win.</li>
		<li>6.	Stand Alone .EXE with GUI</li>
	</ul>
